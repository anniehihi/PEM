    public void search() {
        DefaultTableModel dtm = (DefaultTableModel) this.jTable1moneyinves.getModel();
        dtm.fireTableDataChanged();
        TableRowSorter sorter = new TableRowSorter(dtm);
        this.jTable1moneyinves.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter(this.jTextField3search.getText()));
    }

